var lines = [];
var reader = require('readline').createInterface({
  input: process.stdin,
  output: process.stdout
});
reader.on('line', (line) => {
  lines.push(line);
});
reader.on('close', () => {
    function slice (n) {
        return ("00" + n).slice(-3);
    }
    console.log(slice(Number(lines[0])));
});